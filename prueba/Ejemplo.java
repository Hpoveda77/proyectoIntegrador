class Ejemplo {
    int numero;
    String nombre;
    
    public Ejemplo(int numero, String nombre) {
        this.numero = numero;
        this.nombre = nombre;
    }
    
    public int getNumero() {
        return numero;
    }
    
    public String getNombre() {
        return nombre;
    }
    
    public void setNumero(int numero) {
        this.numero = numero;
    }
    
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    
}